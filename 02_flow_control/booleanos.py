###
# 01 - Booleanos
# Valores logicos: True(Verdadero) y False(falso)
###

print('Valores booleanos')

print(True)
print(False)

#operaciones que nos devuelven valores booleanos
print("\nValores de Comparacion \n")

print('5 > 3:', 5 > 3) #true
#Operaciones de desigualdad
print('5 < 3:', 5 < 3) #false
print("\n")
print('5 == 3:', 5 == 3) #False (desigualdad)
print('3 == 3:', 3 == 3) #true  (igualdad)
print("\n")
print('5 != 3:', 5 != 3) #verdadedo (igualdad)
print('3 != 3:', 3 != 3) #false  (desigulada)



print("\nComparacion de cadenas de text \n")
print("'manzana' < 'pera': ", 'manzana' < 'pera' ) #Verdadero
print('"hola" == "Hola"', "hola" == "Hola") #false
print("\n")


